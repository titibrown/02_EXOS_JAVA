public class Variablesetconstantes {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// uneVariableUnPeuLongue (camel case)
		// UneVariable (Pascal case) csharp
		// une_variable (snake case) python
		
		

		// Declaration Variable + affichage
		int ageUtilisateur = 25;
		System.out.println(ageUtilisateur);
		
		
		ageUtilisateur = 24;
		System.out.println(ageUtilisateur);
		
		
		//constante
		
		final int NUMBER = 26;
		System.out.println(NUMBER);
		
		// Ne peut pas être changé
		//NUMBER = 14;
		//System.out.println(NUMBER);
	}

}
